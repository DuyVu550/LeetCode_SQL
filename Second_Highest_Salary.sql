Select COALESCE(
    (Select salary as SecondHighestSalary 
     from Employee 
     order by salary ASC
     limit 1,1), null
) as 'SecondHighestSalary' 

-- Example 1:

-- Input: 
-- Employee table:
-- +----+--------+
-- | id | salary |
-- +----+--------+
-- | 1  | 100    |
-- | 2  | 200    |
-- | 3  | 300    |
-- +----+--------+
-- Output: 
-- +---------------------+
-- | SecondHighestSalary |
-- +---------------------+
-- | 200                 |
-- +---------------------+
